export const DISTRICT_KEYS = Object.keys(GUJARAT_LOCATIONS);

export function getDistrictItems() {
    return DISTRICT_KEYS.map((key) => ({
        value: key,
        label: GUJARAT_LOCATIONS[key].label,
    }));
}

export function getTalukaItems(districtKey: string) {
    const district = GUJARAT_LOCATIONS[districtKey];
    if (!district) return [];
    return Object.keys(district.talukas).map((key) => ({
        value: key,
        label: district.talukas[key].label,
    }));
}

export function getVillageItems(districtKey: string, talukaKey: string): VillageItem[] {
    return GUJARAT_LOCATIONS[districtKey]?.talukas[talukaKey]?.villages ?? [];
}

export function getGujaratiLabel(districtKey: string, talukaKey?: string, villageKey?: string) {
    const district = GUJARAT_LOCATIONS[districtKey];
    if (!district) return { district: districtKey, taluka: talukaKey ?? "", village: villageKey ?? "" };

    const districtLabel = district.label;
    if (!talukaKey) return { district: districtLabel, taluka: "", village: "" };

    const taluka = district.talukas[talukaKey];
    const talukaLabel = taluka?.label ?? talukaKey;
    if (!villageKey) return { district: districtLabel, taluka: talukaLabel, village: "" };

    const village = taluka?.villages.find((v) => v.value === villageKey);
    return { district: districtLabel, taluka: talukaLabel, village: village?.label ?? villageKey };
}